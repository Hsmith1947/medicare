    			<h1>Notice of Privacy Practices for Original Medicare </h1>
    		</div>
    		    		<div id="content-column" class="span6">
						                <section class="content">
							    				
<p><strong>This notice describes how medical information about you may be used and disclosed and how you can get access to this information. Review it carefully.</strong></p>
<p>The law requires Medicare&nbsp;to protect the privacy of your personal medical information. It also requires us to give you this notice so you know how we may use and&nbsp;share ("disclose")&nbsp;the personal medical information we have about you.</p>
<p><strong>We must provide your information to:</strong></p>
<ul>
<li>You, someone you name ("designate"), or someone who has the legal right to act for you (your personal representative)</li>
<li>The Secretary of the Department of Health and Human Services (HHS), if necessary</li>
<li>Anyone else that the law requires to have it</li>
</ul>
<p><strong>We have&nbsp;the right to use and&nbsp;provide your information to pay for your health care and to operate Medicare. For example:</strong></p>
<ul>
<li>Medicare Administrative Contractors use your information to pay or deny your claims, collect your premiums, share your benefit payment with your other insurer(s), or prepare your "<a href="/forms-help-and-resources/mail-about-medicare/medicare-summary-notice-msn.html">Medicare Summary Notice</a>."</li>
<li>We may use your information to provide you with customer services, resolve complaints you have, contact you about research studies, and make sure you get quality care.</li>
</ul>
<p><strong>We&nbsp;may use or&nbsp;share your information under&nbsp;these limited circumstances:</strong></p>
<ul>
<li>To State and other Federal agencies that have the legal right to&nbsp;get Medicare data (like to make sure Medicare is making proper payments and to&nbsp;help Federal/State Medicaid programs)</li>
<li>For public health activities (like reporting disease outbreaks)</li>
<li>For government health care oversight activities (like investigating&nbsp;fraud and abuse)</li>
<li>For judicial and administrative proceedings (like responding&nbsp;to a court order)</li>
<li>For law enforcement purposes (like providing limited information to&nbsp;find a missing person)</li>
<li>For research studies that meet all privacy law requirements (like research to prevent a disease or disability)</li>
<li>To avoid a serious and imminent threat to health or safety</li>
<li>To contact you about new or changed Medicare benefits</li>
<li>To create a collection of information that no one can trace to you</li>
<li>To practitioners and their contractors for care coordination and quality improvement purposes, like Accountable Care Organizations (ACOs)</li>
</ul>
<p>We must have your written permission (an "authorization") to use or&nbsp;share your information for any purpose that isn't set out in this notice.&nbsp;We don't&nbsp;sell or use and&nbsp;share your information to tell you about health products or services ("marketing").&nbsp;You may take back ("revoke") your written permission at any time, unless we've already shared information because you gave us permission.</p>
<p><strong>You have the right to:</strong></p>
<ul>
<li>See and get a copy of the information we have about you.</li>
<li>Have us change your information if you think it's wrong or incomplete, and we agree. If&nbsp;we disagree, you may have a statement of your disagreement added to your information.</li>
<li>Get a list of&nbsp;people who get your information from us. The listing won't cover information that we gave to you,&nbsp;your personal representative, law enforcement, or to pay for your care or for our operations.</li>
<li>Ask&nbsp;us&nbsp;to communicate with you in a different manner or at a different place (for example, by sending materials to a P.O. Box instead of your home address).</li>
<li>Ask&nbsp;us to limit how we use your information and&nbsp;how we give it&nbsp;out to pay claims and run Medicare. We may not be able to agree to your request.</li>
<li>Get a letter that tells you about the likely risk to the privacy of your information ("breach notification").</li>
<li>Get a separate paper copy of this notice.</li>
</ul>
<p><strong>You may file a privacy complaint with:</strong></p>
<ul>
<li>The Centers for Medicare &amp; Medicaid Services (CMS). Visit Medicare.gov, or call us at 1-800-MEDICARE (1-800-633-4227).&nbsp;</li>
<li>The U.S. Department of Health and Human Services (HHS), Office for Civil Rights (OCR). Visit hhs.gov/hipaa/filing-a-complaint.</li>
</ul>
<p>Filing a complaint won&rsquo;t affect your coverage under Medicare.</p>
<p>To speak to a Customer Service Representative about our privacy notice, call 1-800-MEDICARE.</p>
<p>The law requires us to follow the terms in this notice. We have the right to change the way we use or share your information. If&nbsp;we make a&nbsp;change, we'll mail you a notice within 60 days of the change.</p>
<p>The Notice of Privacy Practices for Original Medicare became effective September 23, 2013.</p>
